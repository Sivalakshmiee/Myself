public class Switch {
    public static void main(String[] args) {
        int n = 1;
        switch (n) {
            case 1:
                System.out.println("I am number one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            case 4:
                System.out.println("four");
                break;
            default:
                System.out.println("Number is not");

        }
    }
}
